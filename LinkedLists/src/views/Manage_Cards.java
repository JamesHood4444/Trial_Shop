    public void display_in_order(DefaultListModel cards, Node node)
    {
        
        if(node != null)
        {
            display_in_order(cards, node.leftNode);
            cards.addElement(String.valueOf(node.value)); 
            display_in_order(cards, node.rightNode);
        }
        
    }
    
